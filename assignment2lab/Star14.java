class Star14{
public static void main(String args[]){
for(int i=1;i<5;i++)
{ for(int m=1;m<=i;m++)
  {
    System.out.print(" ");
  } 	
  for(int j=5;j>=i;j--)
  { 
    System.out.print("* "); 
	}
	System.out.println();
	}
	for(int k=5;k>=1;k--)
	{  for(int m=1;m<=k;m++)
      {
    System.out.print(" ");
      } 
	  for(int l=5;l>=k;l--)
	  {
	    System.out.print("* ");
		}
		System.out.println();
		}
		}
}